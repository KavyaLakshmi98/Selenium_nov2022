package Interview;

public class Pattern_Number {

	public static void main(String[] args) {
       int i,r = 6,j = 0;
		for(i=1;i<=r;i++)
		{
			for(j=1;j<i;j++)
			{
				System.out.println();
				
		        for(j=1;j<=r;j++)
			     {
		        	System.out.print(j+" ");
			   }
			}
		}
		
	}
}
       

